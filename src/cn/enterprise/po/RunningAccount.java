package cn.enterprise.po;

import java.util.Date;

public class RunningAccount {

	private int id;
	//采用双向一对多
	private User user;
	private double amount;
	private int type;
	private Date time;
	private String remark;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	@Override
	public String toString() {
		return "RunningAccount [id=" + id + ", user=" + user + ", amount=" + amount + ", type=" + type + ", time="
				+ time + ", remark=" + remark + "]";
	}
	
}
